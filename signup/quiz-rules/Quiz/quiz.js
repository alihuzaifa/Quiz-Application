var timerSeconds = document.querySelector(".second");
var timerMinutes = document.querySelector(".minutes");
var min = 09;
var sec = 60;
function timer(){
    sec--
    timerSeconds.innerHTML = sec;
    if(sec < 10){
        timerSeconds.innerHTML = "0"+sec
    }
    if(sec === 0){
        min--
        timerMinutes.innerHTML = "0" + min
        sec = 60
    }
    if(min == 0){
        clearInterval(timerOnLoad())
        window.location.replace("User-Greet/greet-user.html")
    }
}
function timerOnLoad(){
var stopTimer = setInterval(timer,1000)
}
