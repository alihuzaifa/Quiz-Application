var getStorage = localStorage.getItem("checkQuiz");

// Update QuestionNumber
var updateNumber = document.querySelector(".updateNumber");

// UpdateNumberCounter 
var UpdateNumberCounter = 1;

// reload function start from here
function reloadQuestion(){
    var quesEl = document.querySelector(".question");
    var el1 = document.querySelector(".ans-1").nextSibling;
    var el2 = document.querySelector(".ans-2").nextSibling;
    var el3 = document.querySelector(".ans-3").nextSibling;
    var el4 = document.querySelector(".ans-4").nextSibling;

    // All radios should be false when the function have called
    var radioButton1 = document.querySelector(".ans-1")
    var radioButton2 = document.querySelector(".ans-2")
    var radioButton3 = document.querySelector(".ans-3")
    var radioButton4 = document.querySelector(".ans-4")
    radioButton1.checked = false;
    radioButton2.checked = false;
    radioButton3.checked = false;
    radioButton4.checked = false;



    // Putting data into short form process
    var makeEasy1 = htmlObject[questionCount];
    var makeEasy2 = cssObject[questionCount];
    var makeEasy3 = javaObject[questionCount];

    // If user choose HTML quiz it will display html quiz
    if(getStorage === "HTML"){
        quesEl.innerHTML = makeEasy1.question;
        el1.innerHTML = makeEasy1.options[0];
        el2.innerHTML = makeEasy1.options[1];
        el3.innerHTML = makeEasy1.options[2];
        el4.innerHTML = makeEasy1.options[3];
        updateNumber.innerHTML = UpdateNumberCounter++   
    }

    // If user choose CSS quiz it will display css quiz
    else if(getStorage === "CSS"){
        quesEl.innerHTML = makeEasy2.question;
        el1.innerHTML = makeEasy2.options[0];
        el2.innerHTML = makeEasy2.options[1];
        el3.innerHTML = makeEasy2.options[2];
        el4.innerHTML = makeEasy2.options[3];
        updateNumber.innerHTML = UpdateNumberCounter++;
    }

    // If user choose JAVASCRIPT quiz it will display javascript quiz
    else if(getStorage === "JAVASCRIPT"){
        quesEl.innerHTML = makeEasy3.question;
        el1.innerHTML = makeEasy3.options[0];
        el2.innerHTML = makeEasy3.options[1];
        el3.innerHTML = makeEasy3.options[2];
        el4.innerHTML = makeEasy3.options[3];
        updateNumber.innerHTML = UpdateNumberCounter++;
    }

}
// Reload functio ends here

// We call because we have to display the question according to user choice
reloadQuestion()


// function for checking answer process start here and it will return answer
function quizAnswer(){
    // Variable for matching the answer
    var ans;

    var answers = document.querySelectorAll(".answer");
    for(var i = 0; i < answers.length; i++){
        if(answers[i].checked){
            ans = answers[i];
            break;
        }
    };
    return ans
}
// function for checking answer process end here

// match answer with return answer from quiz answer function process start here
function getCheckAnswer(){
    var checkedAnswer = quizAnswer();
    // Html Score
    if(getStorage === "HTML"){
        if(checkedAnswer.nextSibling.innerHTML === htmlObject[questionCount].answer){
            score++
        };
        questionCount++
        if(questionCount < htmlObject.length){
            reloadQuestion()
        }
        else{
            attachedScoreOnObj();
            clearInterval(timerOnLoad());
            window.location.replace("User-Greet/greet-user.html");

        };
    }


    // Css Checker
    else if(getStorage === "CSS"){
        if(checkedAnswer.nextSibling.innerHTML === cssObject[questionCount].answer){
            score++
        };
        questionCount++
        if(questionCount < cssObject.length){
            reloadQuestion()
        }
        else{
            attachedScoreOnObj()
            clearInterval(timerOnLoad());
            window.location.replace("User-Greet/greet-user.html");
        };
    }


    // Javascript checker
    else if(getStorage === "JAVASCRIPT"){
        if(checkedAnswer.nextSibling.innerHTML === javaObject[questionCount].answer){
            score++
        };
        questionCount++
        if(questionCount < javaObject.length){
            reloadQuestion()
        }
        else{
            attachedScoreOnObj()
            clearInterval(timerOnLoad());
            window.location.replace("User-Greet/greet-user.html");
        };
    }

    
}
// match answer with return answer from quiz answer function process end here


