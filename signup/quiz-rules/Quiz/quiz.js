// attached user score to his data object in an array in local storage starts from here
function attachedScoreOnObj(){
    var data = localStorage.getItem("data");
    data = JSON.parse(data)
    var getFirst = data[data.length-1]
    getFirst.score = score
    data.pop()
    data.push(getFirst)
    data = JSON.stringify(data)
    localStorage.setItem("data",data)
}
// attached user score to his data object in an array in local storage ends from here

